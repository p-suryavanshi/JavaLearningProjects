package Basic;

public class WhileLoop {

    static void main(String[] args) {

        System.out.println("\n--- Welcome to While Loop ---\n");

        int num = 0;

        while ( num <= 10){
            System.out.println(num);
            num++;
        }

        int i = 500;

        while ( i >= 400 ){
            System.out.println(i);
            i = i - 1;
        }

        int num1 = 100;

        while ( num1 >= 50 ){
            System.out.println(num1);
            num1--;
        }

    }
}
